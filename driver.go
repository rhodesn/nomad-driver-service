// SetConfig is called by the client to pass the configuration for the plugin.
func (d *Driver) SetConfig(cfg *base.Config) error {
	var config PluginConfig
	if len(cfg.PluginConfig) != 0 {
		if err := base.MsgPackDecode(cfg.PluginConfig, &config); err != nil {
			return err
		}
	}

	// Save the configuration to the plugin
	d.config = &config

	// TODO: parse and validated any configuration value if necessary.
	if err := config.validate(); err != nil {
		d.logger.Error("config validation failed")
		return err
	}

	// Save the Nomad agent configuration
	if cfg.AgentConfig != nil {
		d.nomadConfig = cfg.AgentConfig.Driver
	}

	// TODO: initialize any extra requirements if necessary.
	conn, err := systemd.NewSystemConnectionContext(d.ctx)
	if err != nil {
		return fmt.Errorf("failed to connect to dbus %s", err)
	}
	d.conn = conn

	d.logger.Debug("plugin config loaded")

	return nil
}

